"""## Step 4 — Calculate My_ESOL_Solubility

This step rebuilds Delaney's ESOL formula directly from the dataset's own descriptor columns, producing a custom solubility estimate for each compound:
$$\text{My_ESOL_Solubility} = 0.16 - (0.63 \times \text{Calculated_LogP}) - (0.0062 \times \text{Molecular Weight}) + (0.066 \times \text{Number of Rotatable Bonds}) - (0.74 \times \text{Aromatic_Proportion})$$
"""

# Apply the ESOL formula row by row using pandas operations.
df['ESOL_Predicted_LogS'] = (
    0.16
    - (0.63 * df['Calculated_LogP'])
    - (0.0062 * df['Molecular Weight'])
    + (0.066 * df['Number of Rotatable Bonds'])
    - (0.74 * df['Aromatic_Proportion'])
 )

# Check a random sample of 10 rows to confirm the new column looks reasonable.
df[['Compound ID', 'smiles', 'Calculated_LogP', 'Molecular Weight', 'Number of Rotatable Bonds', 'Aromatic_Proportion', 'ESOL_Predicted_LogS']].sample(10)

"""## Step 5 — Classify Solubility Status

Compounds are labeled as **Acceptable** or **Poor** based on a threshold of -4.0 on the `ESOL_Predicted_LogS` scale (compounds with predicted log solubility below -4 are generally considered poorly soluble for oral drug development).
"""

# Classify each compound using apply() with a lambda function, as required.
df['Solubility_Status'] = df['ESOL_Predicted_LogS'].apply(
    lambda value: "Acceptable👍" if value >= -4.0 else "Poor👎"
    )

# Confirm the new column with a random sample.
df[['Compound ID','smiles','ESOL_Predicted_LogS', 'Solubility_Status']].sample(10)

